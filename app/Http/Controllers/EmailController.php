     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $email = new Email();
        $email->email = $request->input('email');
        $email->company_id = $request->input('company_id');
        $email->save();
        return $email;
    }

