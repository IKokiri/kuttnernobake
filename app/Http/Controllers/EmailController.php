    public function updatePadrao(Request $request, $id){

        Email::where('company_id',$request->input('company_id'))->update(['default'=>false]);
        $email = Email::find($id);
        $email->default = true;
        $email->save();

        return $email;
    }
    /**
     * Remove the specified resource from storage.
